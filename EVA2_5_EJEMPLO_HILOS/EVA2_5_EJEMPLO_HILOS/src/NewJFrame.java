    private void actualizarUI(){
        SwingUtilities.invokeLater(new Runnable(){
            @Override
            public void run() {
                String texto = jTextArea1.getText();
                int espaciosBlanco = 0; //Se declara una variable espaciosBlanco para contar la cantidad de espacios en blanco en el texto
                for (char c : texto.toCharArray()) { //Este bucle verifica cada car√°cter para determinar si es un espacio en blanco.
                    if (Character.isWhitespace(c)) {
                        espaciosBlanco++;
                    }
                }
                lblCont.setText("Espacios en blanco: " + espaciosBlanco);
              lblOla.setText("" + jTextArea1.getText().length());
            }
        });
    }

